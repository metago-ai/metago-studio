/**
 * MCP 工具实现库
 *
 * 每个 metago_* 工具的真实执行逻辑。
 * 与 @metago-ai/mcp-server（发布到 npm 的 MCP 服务器）行为对齐：
 * AI 通过 function call 调用工具 → 工具返回该思维协议的完整执行指令 →
 * AI 在下一轮推理中应用对应的方法论。
 *
 * 这是"思维工具"——与文件系统/Git 这种"执行工具"不同，
 * 它们改变 AI 的思考方式，而不是改变外部世界。
 */

/** 单个工具的执行结果 */
export interface MCPToolResult {
  /** 工具是否成功执行 */
  ok: boolean
  /** 思维协议内容（AI 会在下一轮应用） */
  protocol: string
  /** 简短结果（用于日志展示） */
  summary: string
}

/** 输入参数类型 */
export interface MCPToolInput {
  text?: string
  scenario?: string
  target?: string
  depth?: string
  [k: string]: unknown
}

