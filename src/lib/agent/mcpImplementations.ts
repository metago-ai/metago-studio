/**
 * 执行 MCP 工具
 *
 * 与 executeTool 集成。返回思维协议内容（AI 在下一轮应用）。
 */
export function executeMCPTool(
  toolName: string,
  args: Record<string, unknown>,
): MCPToolResult {
  const impl = MCP_TOOL_IMPLEMENTATIONS[toolName]
  if (!impl) {
    return {
      ok: false,
      summary: `工具 ${toolName} 未实现`,
      protocol: `工具 ${toolName} 暂未实现。请使用其他工具。`,
    }
  }
  return impl(args as MCPToolInput)
}
